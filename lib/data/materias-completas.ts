// Funciones de utilidad para buscar materias
export const buscarMateriaPorCodigo2023 = (codigo: string): MateriaCompleta | undefined => {
  return materiasCompletas.find(materia => materia.codigo2023 === codigo)
}

export const buscarMateriaPorCodigo1985 = (codigo: string): MateriaCompleta | undefined => {
  return materiasCompletas.find(materia => materia.codigo1985 === codigo)
}

export const buscarMateriaPorNombre = (nombre: string): MateriaCompleta | undefined => {
  const nombreNormalizado = nombre.toLowerCase().trim()
  return materiasCompletas.find(materia => 
    materia.nombrePlan2023.toLowerCase().includes(nombreNormalizado) ||
    materia.nombrePlan1985.toLowerCase().includes(nombreNormalizado) ||
    materia.nombreAbreviado2023.toLowerCase().includes(nombreNormalizado)
  )
}

export const obtenerMateriasPorPlanYOrientacion = (
  plan: "2023" | "1985",
  orientacion: "profesorado" | "sociocultural" | "arqueologia"
): MateriaCompleta[] => {
  return materiasCompletas.filter(materia => {
    if (plan === "2023") {
      switch (orientacion) {
        case "profesorado":
          return materia.cicloAreaProf2023 !== ""
        case "sociocultural":
          return materia.cicloAreaLicSocio2023 !== ""
        case "arqueologia":
          return materia.cicloAreaLicArqueo2023 !== ""
        default:
          return false
      }
    } else {
      switch (orientacion) {
        case "profesorado":
          return materia.cicloAreaProf1985 !== ""
        case "sociocultural":
          return materia.cicloAreaLicSocio1985 !== ""
        case "arqueologia":
          return materia.cicloAreaLicArqueo1985 !== ""
        default:
          return false
      }
    }
  })
}
